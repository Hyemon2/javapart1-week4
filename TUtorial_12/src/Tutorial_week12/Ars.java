package Tutorial_week12;

public class Ars {
    public int add(int a, int b) {
        return a + b;
    }

    public int add(int a, int b, int c) {
        return a + b + c;
    }

    public String add(String a, String b) {
        return a + " " + b;
    }

    public String add(String a, String b, String c) {
        return a + b + c;
    }
}

